import java.io.*;
import java.util.*;

class KickStart
{
    public static void main(String[] args)
    {
        try (Scanner in = new Scanner(System.in))
        {
            int cases = in.nextInt();
            for (int t = 1; t <= cases; t++)
            {
                System.out.println("Case #" + t + ": ");
            }
        }
    }
}
